"""Correctness evaluation pipeline for the Knowledge QA agent."""
import asyncio
import logging
from typing import Any

import click
from aieng.agent_evals.evaluation import run_experiment
from aieng.agent_evals.evaluation.graders import create_llm_as_judge_evaluator
from aieng.agent_evals.evaluation.graders.config import LLMRequestConfig
from aieng.agent_evals.knowledge_qa.agent import KnowledgeGroundedAgent
from dotenv import load_dotenv
from langfuse.experiment import Evaluation
from rich.console import Console
from rich.panel import Panel
from rich.table import Table

load_dotenv(verbose=True)
logger = logging.getLogger(__name__)
console = Console(width=100)

CORRECTNESS_RUBRIC = """
SYSTEM ROLE:
You are an expert fact-checking evaluator for a retrieval-based AI agent.

TASK:
Evaluate the factual correctness and reasoning quality of the response generated by a retrieval-based, tool-using AI agent.

CONTEXT:
The agent is a tool-based system that retrieves and synthesizes information from external sources.
Your evaluation MUST consider whether the answer is supported by reliable, verifiable information.

SCORING RULES:
- Use binary scoring (0 or 1) for each metric.
- Provide a one-sentence justification for each score.
- Do NOT be lenient: any unsupported or incorrect claim must be penalized.

METRICS:

1. is_factually_correct
   - 1 if ALL claims in the response are factually accurate
   - 0 if ANY claim is incorrect, misleading, or unverifiable

2. no_misleading_information
   - 1 if the response does not contain partial truths that mislead
   - 0 if any statement is misleading in context

3. reasoning_soundness
   - 1 if reasoning steps are logically valid and support the final answer
   - 0 if reasoning is flawed, incomplete, or unsupported

EVALUATION GUIDELINES:
- If ANY hallucination is detected → is_factually_correct = 0
- Extra correct information is acceptable, but unsupported claims are not
- Prioritize grounding over verbosity or style
"""

def create_correctness_evaluator(temperature: float = 0.0) -> Any:
    return create_llm_as_judge_evaluator(
        name="correctness_judge",
        rubric_markdown=CORRECTNESS_RUBRIC,
        model_config=LLMRequestConfig(temperature=temperature),
    )

async def run_local(question: str) -> list[Evaluation]:
    agent = KnowledgeGroundedAgent(enable_planning=True)
    response = await agent.answer_async(question)

    evaluator = create_correctness_evaluator()
    return await evaluator(input=question, output=response.text, expected_output="", metadata=None)

async def _agent_task(*, item: Any, **kwargs: Any) -> str:
    agent = KnowledgeGroundedAgent(enable_planning=True)
    response = await agent.answer_async(item.input)
    return response.text

def run_experiment_eval(dataset_name: str, experiment_name: str):
    evaluator = create_correctness_evaluator()

    run_experiment(
        dataset_name,
        name=experiment_name,
        task=_agent_task,
        evaluators=[evaluator],
    )

@click.command()
@click.option("--local", is_flag=True)
@click.option("--question", default="What is the Federal Reserve?")
@click.option("--dataset-name", default="DeepSearchQA-Sun-Life_with_tools")
@click.option("--experiment-name", default="correctness-eval")
def cli(local, question, dataset_name, experiment_name):
    if local:
        asyncio.run(run_local(question))
    else:
        run_experiment_eval(dataset_name, experiment_name)

if __name__ == "__main__":
    cli()